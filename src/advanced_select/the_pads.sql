SELECT 
  CONCAT(
    NAME, 
    '(', 
    LEFT(OCCUPATION, 1), 
    ')'
  ) 
FROM 
  OCCUPATIONS 
ORDER BY 
  NAME;
SELECT 
  CONCAT(
    'THERE ARE A TOTAL OF ', 
    COUNT(OCCUPATION), 
    ' ', 
    LOWER(OCCUPATION), 
    'S.'
  ) 
FROM 
  OCCUPATIONS 
GROUP BY 
  OCCUPATION 
ORDER BY 
  COUNT(OCCUPATION), 
  OCCUPATION;

